package org.jboss.shrinkwrap.resolver.impl.maven;

import java.io.File;

public class ResolutionResult {

	public final String coordinate;
	public final String source;
	public final File file;

	public ResolutionResult(String coordinate, String source, File file) {
		this.coordinate = coordinate;
		this.source = source;
		this.file = file;
	}

	// TODO How to make this work under Windows?
	public String getPomUrl() {
		String f = file.toString();
		String marker = "/.m2/repository/";
		int idx = f.indexOf(marker);
		return source + f.substring(idx + marker.length() - 1);
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((coordinate == null) ? 0 : coordinate.hashCode());
		result = prime * result + ((file == null) ? 0 : file.hashCode());
		result = prime * result + ((source == null) ? 0 : source.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ResolutionResult other = (ResolutionResult) obj;
		if (coordinate == null) {
			if (other.coordinate != null)
				return false;
		} else if (!coordinate.equals(other.coordinate))
			return false;
		if (file == null) {
			if (other.file != null)
				return false;
		} else if (!file.equals(other.file))
			return false;
		if (source == null) {
			if (other.source != null)
				return false;
		} else if (!source.equals(other.source))
			return false;
		return true;
	}
}